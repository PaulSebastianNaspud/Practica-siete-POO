    private void btnActualizarDiscoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarDiscoActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        this.cantante = controladorCantante.buscarCantante(codigo);
        if (validarCamposDisco()) {
            int codigoCancion = Integer.parseInt(txtCodigoDisco.getText());
            int anioDeLanzamiento = Integer.valueOf(txtAnioDeLanzamiento.getText());
            String nombreDisco = txtNombreDisco.getText();
            controladorCantante.actualizarDisco(cantante, codigo, nombreDisco, anioDeLanzamiento);
            JOptionPane.showMessageDialog(this, "El disco " + nombreDisco + " a sido actualizado");
            cambiaEstadoDisco(false);
            cambiarEstado(false);
            limpiarCamposDisco();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "No se han ingresado todos los datos");
        }
    }//GEN-LAST:event_btnActualizarDiscoActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        this.setVisible(false);
        this.limpiarCampos();
        this.limpiarCamposDisco();
        this.cambiarEstado(false);
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnBuscarCompositorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCompositorActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        this.cantante = controladorCantante.buscarCantante(codigo);
        if (cantante != null) {
            txtApellido.setText(cantante.getApellido());
            txtNombre.setText(cantante.getNombre());
            cambiarEstado(true);
        }
    }//GEN-LAST:event_btnBuscarCompositorActionPerformed

    private void btnCancelar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelar1ActionPerformed
        cambiarEstado(false);
        cambiaEstadoDisco(false);
        limpiarCampos();
    }//GEN-LAST:event_btnCancelar1ActionPerformed

    private void btnBuscarDiscoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDiscoActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        this.cantante = controladorCantante.buscarCantante(codigo);
        int codigoDisco = Integer.parseInt(txtCodigoDisco.getText());
        Disco disco = controladorCantante.buscarDisco(cantante, codigo);
        if (disco != null) {
            txtAnioDeLanzamiento.setText(String.valueOf(disco.getAnioDeLanzamiento()));
            txtNombreDisco.setText(disco.getNombre());
            cambiaEstadoDisco(true);
        } else {
            limpiarCamposDisco();
            JOptionPane.showMessageDialog(this, "No se ha encotrado el disco");
        }
    }//GEN-LAST:event_btnBuscarDiscoActionPerformed

    private void cambiarEstado(boolean bandera){
        txtCodigo.setEnabled(!bandera);
        txtCodigoDisco.setEnabled(bandera);
        btnBuscarDisco.setEnabled(bandera);
    }
    private void cambiaEstadoDisco(boolean bandera){
        txtCodigoDisco.setEnabled(!bandera);
        txtAnioDeLanzamiento.setEnabled(bandera);
        txtNombreDisco.setEnabled(bandera);
        btnActualizarDisco.setEnabled(bandera);
        
    }
    
    private void limpiarCampos(){
        txtCodigo.setText("");
        txtApellido.setText("");
        txtNombre.setText("");
        
    }
    private boolean validarCamposDisco() {
        if (txtNombreDisco.getText().isEmpty() || txtCodigoDisco.getText().isEmpty() || txtAnioDeLanzamiento.getText().isEmpty()) {
            return false;
        } return true;
    }
    private void limpiarCamposDisco(){
        txtCodigoDisco.setText("");
        txtAnioDeLanzamiento.setText("");
        txtNombreDisco.setText("");
    }

