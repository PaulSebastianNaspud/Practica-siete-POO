    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        this.cantante = controladorCantante.buscarCantante(codigo);
        if (cantante != null) {
            txtApellido.setText(cantante.getApellido());
            txtNombre.setText(cantante.getNombre());
            txtNombreArtistico.setText(cantante.getNombreArtistico());
            cambiarEstado(false);
        } else {
            JOptionPane.showMessageDialog(this, "El cantante con el codigo " + codigo + " no ha sido registrado");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        this.cantante = controladorCantante.buscarCantante(codigo);
        int respuesta = JOptionPane.showConfirmDialog(this, "Â¿Quieres elimar de la lista a: " + cantante.getNombre() + " " + cantante.getApellido() + "?");
        if (respuesta == JOptionPane.YES_OPTION) {
            controladorCantante.eliminarCantante(cantante);
            JOptionPane.showMessageDialog(this, "El cantante ha sido eliminado");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "El cantante no ha sido eliminado");
            limpiarCampos();
        }
        cambiarEstado(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void cambiarIdioma(Locale localizacion) {
        mensajes = ResourceBundle.getBundle("mensajes.mensaje", localizacion);
        jblApe.setText(mensajes.getString("jlb.ape"));
        jblCod.setText(mensajes.getString("jlb.cod"));
        jblMenPan.setText(mensajes.getString("menuitem.elican"));
        jblNom.setText(mensajes.getString("jlb.nom"));
        jblNomArt.setText(mensajes.getString("jlb.nomart"));
        btnEliminar.setText(mensajes.getString("btn.eli"));
        btnCancelar.setText(mensajes.getString("btn.can"));
        btnBuscar.setText(mensajes.getString("btn.bus"));
        this.setTitle(mensajes.getString("menuitem.elican"));
        
    }
    
